"""
Tiny pure-stdlib PNG writer.

Used to generate the severity-bucket legend image bundled into the KMZ
ScreenOverlay. We avoid Pillow / numpy / cairo to keep the Render bundle
small — the legend is a fixed-size raster of horizontal color bands with a
hand-traced 5×7 bitmap font for the threshold labels.
"""

from __future__ import annotations

import struct
import zlib


