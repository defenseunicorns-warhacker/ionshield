def _draw_glyph(
    pixels: bytearray, w: int, x: int, y: int, ch: str, color: tuple[int, int, int],
) -> None:
    rows = _FONT.get(ch.upper(), _FONT[" "])
    r, g, b = color
    for row_i, bits in enumerate(rows):
        for col in range(5):
            if bits & (0x10 >> col):  # bit 4 = leftmost
                px = x + col
                py = y + row_i
                idx = (py * w + px) * 4
                pixels[idx]     = r
                pixels[idx + 1] = g
                pixels[idx + 2] = b
                pixels[idx + 3] = 255


def draw_text(
    pixels: bytearray, w: int, x: int, y: int, text: str,
    color: tuple[int, int, int] = (255, 255, 255),
) -> None:
    """Draw a single line of text at (x, y), 5×7 font, 1-pixel tracking."""
    cursor = x
    for ch in text:
        _draw_glyph(pixels, w, cursor, y, ch, color)
        cursor += 6  # 5px glyph + 1px gap


def fill_rect(
    pixels: bytearray, w: int, x: int, y: int, rw: int, rh: int,
    color: tuple[int, int, int, int],
) -> None:
    """Fill a rectangle with RGBA `color` in-place on the pixel buffer."""
    r, g, b, a = color
    for py in range(y, y + rh):
        row_start = (py * w + x) * 4
        for i in range(rw):
            o = row_start + i * 4
            pixels[o]     = r
            pixels[o + 1] = g
            pixels[o + 2] = b
            pixels[o + 3] = a


def encode_png(width: int, height: int, rgba_pixels: bytes | bytearray) -> bytes:
    """
    Encode an RGBA pixel buffer (`width × height × 4` bytes) into PNG bytes.
    """
    if len(rgba_pixels) != width * height * 4:
        raise ValueError("rgba_pixels length mismatch")

    def _chunk(name: bytes, data: bytes) -> bytes:
        crc = zlib.crc32(name + data) & 0xFFFFFFFF
        return struct.pack("!I", len(data)) + name + data + struct.pack("!I", crc)

    sig = b"\x89PNG\r\n\x1a\n"
    # IHDR: width, height, 8-bit, color type 6 (RGBA), no compression/filter/interlace
    ihdr = struct.pack("!IIBBBBB", width, height, 8, 6, 0, 0, 0)

    # PNG requires a per-scanline filter byte (0 = no filter)
    raw_lines = []
    stride = width * 4
    for y in range(height):
        raw_lines.append(b"\x00")
        raw_lines.append(bytes(rgba_pixels[y * stride : (y + 1) * stride]))
    idat = zlib.compress(b"".join(raw_lines), level=9)

    return (
        sig
        + _chunk(b"IHDR", ihdr)
        + _chunk(b"IDAT", idat)
        + _chunk(b"IEND", b"")
    )
